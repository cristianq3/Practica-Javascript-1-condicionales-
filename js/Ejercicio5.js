//Escribe un programa de tres líneas que pida un número, pida otro número y escriba el resultado de sumar estos dos números.

//Se declaran las variables y mediante un prompt le asignamos el nro que ingrese el usuario.
let primerNum = parseInt(prompt("Ingresa el Primer Número"));
let segundoNum = parseInt(prompt("Ingresa el Segundo Número"));

// Se muestra la suma de las variables.
document.write("La suma es " + (primerNum + segundoNum));
